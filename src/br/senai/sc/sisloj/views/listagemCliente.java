    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        Cliente cli = new Cliente();
        cli.setNomcli(cpNome.getText());
        cli.setEndcli(cpEnd.getText());
        cli.setBaicli(cpBai.getText());
        cli.setComcli(cpCom.getText());

        String cep = cpCep.getText();
        cep = cep.replaceAll("[^0-9]", "");

        String celular = cpCel.getText();
        celular = celular.replaceAll("[^0-9]", "");

        cli.setCepcli(Integer.parseInt(cep));
        cli.setCelcli(Integer.parseInt(celular));

        //Inseri o cliente no banco de dados
        ClienteDao cliDao = new ClienteDao();
        try {
            cliDao.inserir(cli);
            JOptionPane.showMessageDialog(null, "Cliente inserido com sucesso!");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Falha ao inserir o cliente!");
            Logger.getLogger(CadastroCliente.class.getName()).log(Level.SEVERE, null, ex);
        }

        //LIMPAR CAMPOS
        cpNome.setText("");
        cpEnd.setText("");
        cpBai.setText("");
        cpCom.setText("");
        cpCep.setText("");
        cpCel.setText("");

    }//GEN-LAST:event_btnSalvarActionPerformed

