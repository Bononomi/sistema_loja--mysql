    private void popularTabela() {
        ClienteDao cl = new ClienteDao();
        List<Cliente> listaCliente;
        try {
            listaCliente = cl.listarClientes();
            DefaultTableModel model = (DefaultTableModel) tblCliente.getModel();
            List<Object> lista = new ArrayList<Object>();
            
            for (int i = 0; i < listaCliente.size(); i++) {
                Cliente c = listaCliente.get(i);
                lista.add(new Object[]{c.getCodcli(), c.getNomcli(), c.getEndcli(),
                    c.getBaicli(), c.getComcli(), c.getCepcli(), c.getCelcli()});

            }

            for (int idx = 0; idx < lista.size(); idx++) {
                model.addRow((Object[]) lista.get(idx));

            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Ocorreu um erro ao obter cliente no banco de dados!");
            Logger.getLogger(listagemCliente.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

